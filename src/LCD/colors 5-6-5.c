/*******************************************************************************
* Function Name  : Color_ChangeBrightness
* Description    :
* Input          : reference color, BrightLevel
* Output         : changed color
* Return         : None
*******************************************************************************/
uint16_t Color_ChangeBrightness(uint16_t ColorIn, int8_t BrightLevel)
{
	int32_t R = 0, G = 0, B = 0;

	if(BrightLevel > 32) BrightLevel = 32;
	else if(BrightLevel < -32) BrightLevel = -32;

	R = ((ColorIn & 0xF800) >> 11) + (BrightLevel);
	G = ((ColorIn & 0x07E0) >> 5) + (BrightLevel * 2);
	B = (ColorIn & 0x001F) + (BrightLevel);

	if(R < 0) R = 0;
	else if(R > 31) R = 31;

	if(G < 0) G = 0;
	else if(G > 63) G = 63;

	if(B < 0) B = 0;
	else if(B > 31) B = 31;


	return (uint16_t)((R << 11) | (G << 5) | (B));
}
